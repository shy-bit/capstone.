    <script>
        document.querySelectorAll('.about-tab').forEach(function(tab) {
            tab.addEventListener('click', function() {
                document.querySelectorAll('.about-tab').forEach(function(item) {
                    item.classList.remove('active');
                });

                document.querySelectorAll('.about-panel').forEach(function(panel) {
                    panel.classList.remove('active');
                });

                this.classList.add('active');
                document.getElementById(this.dataset.target).classList.add('active');
            });
        });

        const counters = document.querySelectorAll('.counter');

        function animateCounter(counter) {
            if (counter.dataset.started === 'true') {
                return;
            }

            counter.dataset.started = 'true';

            const target = Number(counter.dataset.target) || 0;
            const suffix = counter.dataset.suffix || '';
            const prefix = counter.dataset.prefix || '';
            const duration = 1600;
            const step = Math.max(1, Math.ceil(target / 70));
            let current = 0;

            const timer = setInterval(function() {
                current += step;

                if (current >= target) {
                    current = target;
                    clearInterval(timer);
                }

                counter.textContent = prefix + current.toLocaleString('en-US') + suffix;
            }, 24);
        }

        if (counters.length) {
            const counterObserver = new IntersectionObserver(function(entries, observer) {
                entries.forEach(function(entry) {
                    if (entry.isIntersecting) {
                        animateCounter(entry.target);
                        observer.unobserve(entry.target);
                    }
                });
            }, { threshold: 0.4 });

            counters.forEach(function(counter) {
                counterObserver.observe(counter);
            });
        }
    </script>
